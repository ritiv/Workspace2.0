
public class char_Booleans {

	public static void main(String[] args) {
		
		char myChar = 'D';
		System.out.println("My character " + myChar);
		
		int i = myChar;
		System.out.println(i);
		
		char x = 69;
		System.out.println("character x: " + x);
		
		char mynewchar = '\u0044';
		System.out.println("its a unicode value " + mynewchar);
		
		boolean flag = true;
		System.out.println(flag);
		
		

	}

}
